print("------- NUMBERS --------")
# INTEGER
number = 3
print("number1: ", number)
print(type(number))

# Convertir a float
number2 = float(number)
print("number2: ", number2)
print(type(number2))

# Convertir a string
numeroString = str(number)
print("number2: ", numeroString)
print(type(numeroString))

# FLOAT
numberFloat = 3.5
print("numberFloat: ", numberFloat)
print(type(numberFloat))
